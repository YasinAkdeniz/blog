    public function findWithCount()
    {
        $qb = $this->createQueryBuilder("bc");

        return $qb
            ->select(['bc.id', 'bc.slug', 'bc.name', 'COUNT(b.id) as blogCount'])
            ->innerJoin("bc.blogs", 'b')
            ->groupBy('bc.id')
            ->getQuery()
            ->getArrayResult();
    }

